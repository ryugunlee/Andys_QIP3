def _base_code(ticker: str) -> str:
    """"005930.KS" / "005930" -> "005930" (거래소 접미사 제거)."""
    return ticker.split(".", 1)[0]


def display_name(ticker: str, raw_name: str | None) -> str | None:
    """보정맵에 있으면 한글명, 없으면 원래 이름을 그대로 반환한다."""
    override = KOREAN_NAME_OVERRIDES.get(_base_code(ticker))
    if override is not None:
        return override
    return raw_name
